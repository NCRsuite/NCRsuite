create or replace function public.restaurant_order_access_guard()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.organization_has_plan_feature(new.organization_id, 'restaurant_ordering') then
    raise exception 'La prise de commande n’est pas incluse dans cette offre.';
  end if;
  if not public.is_org_member(new.organization_id) then
    raise exception 'Accès refusé.';
  end if;
  if tg_table_name = 'restaurant_order_items'
     and new.status in ('in_progress','ready','served')
     and not public.organization_has_plan_feature(new.organization_id, 'restaurant_kitchen_display') then
    raise exception 'L’écran Cuisine est disponible à partir de l’offre Essentielle.';
  end if;
  return new;
end;
$$;

create or replace function public.recalculate_restaurant_order(p_order_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_org uuid;
  v_total integer;
begin
  select organization_id into v_org from public.restaurant_orders where id = p_order_id;
  if v_org is null then return; end if;

  select coalesce(sum(unit_price_cents * quantity) filter (where status <> 'canceled'), 0)::integer
  into v_total
  from public.restaurant_order_items
  where order_id = p_order_id and organization_id = v_org;

  update public.restaurant_orders
  set subtotal_cents = v_total,
      total_cents = v_total,
      updated_at = now()
  where id = p_order_id;
end;
$$;

create or replace function public.recalculate_restaurant_order_trigger()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if tg_op = 'DELETE' then
    perform public.recalculate_restaurant_order(old.order_id);
    return old;
  end if;
  perform public.recalculate_restaurant_order(new.order_id);
  return new;
end;
$$;

create or replace function public.sync_restaurant_table_from_order()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.table_id is null then return new; end if;

  update public.restaurant_tables
  set service_status = case
    when new.status = 'bill_requested' then 'payment'
    when new.status in ('draft','sent','in_progress','ready','served') then 'ordering'
    when new.status = 'closed' then 'cleaning'
    when new.status = 'canceled' then 'available'
    else service_status
  end,
  updated_at = now()
  where id = new.table_id and organization_id = new.organization_id;

  return new;
end;
$$;

create or replace function public.open_restaurant_order(
  p_organization_id uuid,
  p_table_id uuid default null,
  p_guest_count integer default 1,
  p_reservation_id uuid default null
)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_id uuid;
  v_existing uuid;
begin
  if auth.uid() is null or not public.is_org_member(p_organization_id) then
    raise exception 'Accès refusé.';
  end if;
  if not public.organization_has_plan_feature(p_organization_id, 'restaurant_ordering') then
    raise exception 'La prise de commande n’est pas incluse dans cette offre.';
  end if;

  if p_table_id is not null then
    select id into v_existing
    from public.restaurant_orders
    where organization_id = p_organization_id
      and table_id = p_table_id
      and status in ('draft','sent','in_progress','ready','served','bill_requested')
    order by opened_at desc
    limit 1;
    if v_existing is not null then return v_existing; end if;
  end if;

  insert into public.restaurant_orders(
    organization_id, table_id, reservation_id, guest_count, opened_by
  ) values (
    p_organization_id, p_table_id, p_reservation_id, greatest(1, least(coalesce(p_guest_count,1),100)), auth.uid()
  ) returning id into v_id;

  if p_reservation_id is not null then
    update public.restaurant_reservations
    set status = 'seated', updated_at = now()
    where id = p_reservation_id and organization_id = p_organization_id
      and status in ('pending','confirmed');
  end if;

  return v_id;
end;
$$;

create or replace function public.send_restaurant_order(p_order_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_org uuid;
begin
  select organization_id into v_org from public.restaurant_orders where id=p_order_id;
  if v_org is null or not public.is_org_member(v_org) then raise exception 'Commande introuvable.'; end if;
  if not exists(select 1 from public.restaurant_order_items where order_id=p_order_id and status='draft') then
    raise exception 'Aucun nouvel article à envoyer.';
  end if;

  update public.restaurant_order_items
  set status='sent', sent_at=now(), updated_at=now()
  where order_id=p_order_id and status='draft';

  update public.restaurant_orders
  set status='sent', updated_at=now()
  where id=p_order_id and status not in ('closed','canceled');
end;
$$;

create or replace function public.set_restaurant_order_item_status(p_item_id uuid, p_status text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_org uuid;
  v_order uuid;
begin
  if p_status not in ('sent','in_progress','ready','served','canceled') then
    raise exception 'Statut invalide.';
  end if;
  select organization_id, order_id into v_org, v_order from public.restaurant_order_items where id=p_item_id;
  if v_org is null or not public.is_org_member(v_org) then raise exception 'Article introuvable.'; end if;

  update public.restaurant_order_items
  set status=p_status,
      started_at=case when p_status='in_progress' then coalesce(started_at,now()) else started_at end,
      ready_at=case when p_status='ready' then coalesce(ready_at,now()) else ready_at end,
      served_at=case when p_status='served' then coalesce(served_at,now()) else served_at end,
      canceled_at=case when p_status='canceled' then coalesce(canceled_at,now()) else canceled_at end,
      updated_at=now()
  where id=p_item_id;

  update public.restaurant_orders o
  set status = case
    when exists(select 1 from public.restaurant_order_items i where i.order_id=o.id and i.status='in_progress') then 'in_progress'
    when exists(select 1 from public.restaurant_order_items i where i.order_id=o.id and i.status='sent') then 'sent'
    when exists(select 1 from public.restaurant_order_items i where i.order_id=o.id and i.status='ready') then 'ready'
    when exists(select 1 from public.restaurant_order_items i where i.order_id=o.id and i.status='served') then 'served'
    else o.status end,
    updated_at=now()
  where o.id=v_order and o.status not in ('bill_requested','closed','canceled');
end;
$$;

