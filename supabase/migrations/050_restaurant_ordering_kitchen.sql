create or replace function public.close_restaurant_order_without_payment(p_order_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare v_org uuid;
begin
  select organization_id into v_org from public.restaurant_orders where id=p_order_id;
  if v_org is null or not public.has_org_role(v_org,array['owner','admin','manager']) then
    raise exception 'Seul un responsable peut clôturer la note.';
  end if;
  update public.restaurant_orders
  set status='closed', closed_by=auth.uid(), closed_at=now(), updated_at=now()
  where id=p_order_id and status not in ('closed','canceled');
end;
$$;

